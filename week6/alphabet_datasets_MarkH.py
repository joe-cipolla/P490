#factor is % chance of a pixel adding noise [0,1]
def AddRandomNoise(ds, factor):
    with_noise=[]
    
    ll = len(ds)
    for i in range(ll):
        
        #get entire row
        row = ds[i]
        
        #copy the data part
        data = row[1][:]
        
        for j in range(len(data)):
            z = random.random()
            if z < factor:
                data[j] = data[j]^1
        
        #make a new row
        newrow = (row[0],data,row[2],row[3],row[4],row[5])
        with_noise.append(newrow)
    return with_noise


